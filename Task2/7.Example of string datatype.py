# 7.Example of string datatype
name = 'KUMUD'

# prints full name
print('Name is:', name)

# prints character starting from 2nd to 4th
print(name[1:4])

# prints whole string starting from 2nd character
print(name[1:])

# prints 1st character of string
print(name[0])

# prints string twice
print(name * 2)

# prints concatenated string
print('Hey '+ name)
